package com.github.laxika.magicalvibes.cards.i;

import com.github.laxika.magicalvibes.cards.CardRegistration;
import com.github.laxika.magicalvibes.model.ActivatedAbility;
import com.github.laxika.magicalvibes.model.Card;
import com.github.laxika.magicalvibes.model.effect.DestroyTargetPermanentEffect;
import com.github.laxika.magicalvibes.model.filter.PermanentAllOfPredicate;
import com.github.laxika.magicalvibes.model.filter.PermanentIsCreaturePredicate;
import com.github.laxika.magicalvibes.model.filter.PermanentPowerAtLeastPredicate;
import com.github.laxika.magicalvibes.model.filter.PermanentPredicateTargetFilter;

import java.util.List;

@CardRegistration(set = "8ED", collectorNumber = "26")
public class IntrepidHero extends Card {

    public IntrepidHero() {
        // {T}: Destroy target creature with power 4 or greater.
        addActivatedAbility(new ActivatedAbility(
                true, null,
                List.of(new DestroyTargetPermanentEffect(false)),
                "{T}: Destroy target creature with power 4 or greater.",
                new PermanentPredicateTargetFilter(
                        new PermanentAllOfPredicate(List.of(
                                new PermanentIsCreaturePredicate(),
                                new PermanentPowerAtLeastPredicate(4)
                        )),
                        "Target must be a creature with power 4 or greater"
                )
        ));
    }
}
